# Aritmética:
2 + 5     # Soma
10 - 5    # Subtração
2 * 5     # Multiplicação
10 / 5    # Divisão
3 ** 2    # Potência
100 %% 2  # Módulo (resto da divisão)

# Booleana:
TRUE + TRUE         # = 2 
TRUE + FALSE        # = 1
TRUE + FALSE/FALSE  # NaN
TRUE / FALSE        # INF = INFINITO

!TRUE       # Negação de verdade
!FALSE      # Negação de falso
TRUE | FALSE  # OU lógico
TRUE & FALSE  # E lógico
